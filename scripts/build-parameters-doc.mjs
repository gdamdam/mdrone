#!/usr/bin/env node
// Generates docs/parameters.md from the engine's AudioWorkletProcessor
// parameterDescriptors blocks + the exposed setter clamps in FxChain /
// AudioEngine / MasterBus. Run via `npm run docs:params` or at prebuild
// time. Avoids drift between the code and the reference table by
// reading the source directly.
//
// Extraction strategy is intentionally simple — regex over flat JS/TS.
// If a voice / effect is added, update WORKLET_SOURCES below.

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = join(HERE, "..");
const OUT = join(ROOT, "docs/parameters.md");

const WORKLET_SOURCES = [
  { label: "Drone voice (per voice: tanpura/reed/metal/air/piano/fm/amp)",
    path: "src/engine/voices/core.js" },
  { label: "FX chain (plate / shimmer / freeze / granular / FDN reverb / brickwall limiter)",
    path: "src/engine/fxChainProcessor.js" },
];

function extractParamDescriptors(src) {
  // Find `static get parameterDescriptors() { return [ ... ]; }`
  // blocks and extract every { name, defaultValue, minValue, maxValue } object.
  const out = [];
  const blocks = src.matchAll(/parameterDescriptors[\s\S]*?return\s*\[([\s\S]*?)\];/g);
  for (const m of blocks) {
    const body = m[1];
    const entries = body.matchAll(/\{\s*name:\s*"([^"]+)"[^}]*?defaultValue:\s*([0-9.eE+-]+)[^}]*?minValue:\s*([0-9.eE+-]+)[^}]*?maxValue:\s*([0-9.eE+-]+)[^}]*?\}/g);
    for (const e of entries) {
      out.push({
        name: e[1],
        default: Number(e[2]),
        min: Number(e[3]),
        max: Number(e[4]),
      });
    }
  }
  return out;
}

// Walk each worklet source and collect a processor → params map.
function extractProcessorBlocks(src) {
  // Split by `class XxxProcessor extends AudioWorkletProcessor {`
  const out = [];
  const classRe = /class\s+(\w+Processor)\s+extends\s+AudioWorkletProcessor\s*\{/g;
  const matches = [...src.matchAll(classRe)];
  for (let i = 0; i < matches.length; i++) {
    const start = matches[i].index;
    const end = i + 1 < matches.length ? matches[i + 1].index : src.length;
    const chunk = src.slice(start, end);
    const params = extractParamDescriptors(chunk);
    // Find the registerProcessor("name", Cls) paired with this class.
    const regRe = new RegExp(`registerProcessor\\(\\s*"([^"]+)"\\s*,\\s*${matches[i][1]}\\s*\\)`);
    const reg = src.match(regRe);
    out.push({
      className: matches[i][1],
      processorName: reg?.[1] ?? "(unregistered)",
      params,
    });
  }
  return out;
}

// Drone voice — single class with voiceType dispatch; its parameter
// descriptors apply to every voice.
function extractDroneVoiceProcessor(src) {
  const params = extractParamDescriptors(src);
  return [{
    className: "DroneVoiceProcessor",
    processorName: "drone-voice",
    params,
  }];
}

function renderTable(rows) {
  if (rows.length === 0) return "_(no parameters)_\n";
  const lines = [
    "| Name | Default | Min | Max |",
    "|------|---------|-----|-----|",
  ];
  for (const r of rows) {
    lines.push(`| \`${r.name}\` | ${r.default} | ${r.min} | ${r.max} |`);
  }
  return lines.join("\n") + "\n";
}

let md = `# mdrone — parameter reference

Auto-generated by \`scripts/build-parameters-doc.mjs\`. Do not edit
directly — regenerate via \`npm run docs:params\`.

This file enumerates the \`AudioParam\` descriptors declared on every
AudioWorkletProcessor in the engine. k-rate AudioParams can be
automated via \`setTargetAtTime\` / \`linearRampToValueAtTime\` from
the main thread; see \`FxChain.ts\` / \`MasterBus.ts\` for the
surface-level setters that clamp user input into these ranges.

`;

for (const { label, path } of WORKLET_SOURCES) {
  const full = join(ROOT, path);
  if (!existsSync(full)) continue;
  const src = readFileSync(full, "utf8");
  const blocks = path.endsWith("voices/core.js")
    ? extractDroneVoiceProcessor(src)
    : extractProcessorBlocks(src);
  md += `## ${label}\n\n`;
  md += `Source: \`${path}\`\n\n`;
  for (const b of blocks) {
    md += `### ${b.processorName} (\`${b.className}\`)\n\n`;
    md += renderTable(b.params);
    md += `\n`;
  }
}

// Master bus setters: HPF / EQ / glue / drive / limiter / headphone-safe.
md += `## Master bus setters

Source: \`src/engine/MasterBus.ts\`

| Setter | Range | Default | Notes |
|--------|-------|---------|-------|
| \`setMasterVolume(v)\` | 0 .. 1.5 | 1.0 | Clamped to -6 dBFS when headphone-safe is on |
| \`setHpfFreq(hz)\` | ≥10 Hz | 10 | 10 ≈ bypass; step UI uses 10/20/30/40 |
| \`setGlueAmount(a)\` | 0..1 | 0 | Drives threshold from 0 to -18 dB + makeup 1..1.5× |
| \`setDrive(a)\` | 1..10 | 1.5 | Pre-gain × tanh(k·x) waveshaper, 4096-pt LUT, 2× oversample |
| \`setLimiterEnabled(b)\` | bool | true | Brickwall worklet; instant attack, exp release |
| \`setLimiterCeiling(dB)\` | -24..0 | -1 | Converted to linear for the worklet \`ceiling\` param |
| \`setLimiterRelease(sec)\` | 0.02..1 | 0.12 | Exponential tail after peak detection |
| \`setHeadphoneSafe(on)\` | bool | false | When on, outputTrim is clamped to 0.5 linear ≈ -6 dBFS |
`;

// Engine voice-layer settings
md += `\n## Voice engine

Source: \`src/engine/VoiceEngine.ts\`

| Setter | Range | Default | Notes |
|--------|-------|---------|-------|
| \`setMaxVoiceLayers(n)\` | 1..7 | auto (4 on ≤4-core systems, else 7) | Mobile auto-degrader — see \`detectMaxVoiceLayers\` |
| \`setTanpuraTuning(id)\` | \`classic\` \\| \`sa-pa\` \\| \`sa-ma\` \\| \`sa-ni\` \\| \`sa-ma-pa-ni\` | \`classic\` | Applied at voice construction; triggers a rebuild |
| \`setReedShape(s)\` | \`odd\` \\| \`even\` \\| \`balanced\` \\| \`sine\` | \`odd\` | Applied at voice construction |
| \`setFmRatio(r)\` | 0.5..12 | 2.0 | Modulator:carrier ratio |
| \`setFmIndex(i)\` | 0.1..12 | 2.4 | Modulation index |
| \`setFmFeedback(fb)\` | 0..1 | 0 | Modulator self-feedback |
| \`setTanpuraPluckRate(r)\` | 0..4 | 1 | 0 = infinite hold |
| \`setDrift(v)\` | 0..1 | 0.3 | Per-voice detune amount |
| \`setSub(v)\` | 0..1 | 0 | Dedicated sub-oscillator stack |
| \`setBloom(v)\` | 0..1 | 0.15 | Voice fade-in time scalar |
| \`setGlide(v)\` | 0..1 | 0.15 | Tonic retune time scalar |
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, md);
console.log(`[build-parameters-doc] wrote ${OUT}`);
